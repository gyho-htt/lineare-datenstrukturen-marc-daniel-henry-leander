public class Vokabel {
    private String name;
    private String uebersetzung;

    public Vokabel (String pname, String puebersetzng) {
        this.name = pname;
        this.uebersetzung = puebersetzng;
    }

    public String getName() {
        return name;
    }

    public String getuebersetzung() {
        return uebersetzung;
    }


}
